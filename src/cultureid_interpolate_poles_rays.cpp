/*******************************************************************************
 * FILL IN
 */
void CIPR::interpolateInvalidRaysInScan(const std::vector<float>& scan_in,
  std::vector<float>* scan_out)
{
  scan_out->clear();

  // Identify contiguous regions of zero measurement
  std::vector< std::vector <int> > regions;
  int i = 0;
  int j;
  while(i < scan_in.size())
  {
    if (scan_in[i] == value_invalid_)
    {
      int region_begin = i;
      int region_end;
      bool broke = false;
      for (j = region_begin+1; j < scan_in.size(); j++)
      {
        if (scan_in[j] == value_invalid_)
          continue;
        else
        {
          region_end = j-1;
          {
            broke = true;
            break;
          }
        }
      }

      if (j == scan_in.size() && !broke)
        region_end = j-1;

      std::vector<int> region;
      region.push_back(region_begin);
      region.push_back(region_end);
      regions.push_back(region);

      i = j+1;
    }
    else
      i++;
  }


  // Inflate to consecutive indices
  for (unsigned int i = 0; i < regions.size(); i++)
  {
    int begin = regions[i][0];
    int end = regions[i][regions[i].size()-1];

    regions[i].clear();

    for (unsigned int j = begin; j <= end; j++)
      regions[i].push_back(j);
  }



  // Is the first index 0 and the last equal to the size-1?
  int num_regions = regions.size();
  int numel_last_region = regions[num_regions-1].size();

  if (regions[0][0] == 0 &&
    regions[num_regions-1][numel_last_region-1] == scan_in.size()-1)
  {
    for (int i = 0; i < regions[0].size(); i++)
      regions[num_regions-1].push_back(regions[0][i]);

    regions.erase(regions.begin(), regions.begin()+1);
  }

  /*
     for (int i = 0; i < regions.size(); i++)
     {
     printf("region %d\n", i);
     for (int j = 0; j < regions[i].size(); j++)
     printf("%d,", regions[i][j]);
     printf("\n");
     }
     */

  std::vector<float> scan_in_interp = scan_in;

  for (unsigned int i = 0; i < regions.size(); i++)
  {
    int interp_begin = regions[i][0]-1;
    int interp_end = regions[i][regions[i].size()-1]+1;

    if (interp_begin < 0)
      interp_begin = scan_in.size()-1;

    if (interp_end >= scan_in.size())
      interp_end = 0;

    float range_a = scan_in[interp_begin];
    float range_b = scan_in[interp_end];
    float interp = (range_a + range_b)/2;

    for (unsigned int j = 0; j < regions[i].size(); j++)
      scan_in_interp[regions[i][j]]= interp;
  }

  /*
     for (int j = 0; j < scan_in.size(); j++)
     printf("%f\n", scan_in_interp[j]);
     */

  *scan_out = scan_in_interp;
}


/*******************************************************************************
 * Loads the indices of rays that touch upon poles.
 */
void CIPR::loadInvalidIds()
{
  XmlRpc::XmlRpcValue xml_ids;
  nodehandle_.getParam(ros::this_node::getName() + "/ids", xml_ids);

  if(xml_ids.getType() != XmlRpc::XmlRpcValue::TypeArray)
    ROS_ERROR("param 'invalid_ids' is not a list");
  else
  {
    for(unsigned int i = 0; i < xml_ids.size(); i++)
    {
      if(xml_ids[i].getType() != XmlRpc::XmlRpcValue::TypeInt)
        ROS_ERROR("invalid_ids[%d] is not an int", i);
      else
      {
        int id = xml_ids[i];
        invalid_ids_.push_back(id);
      }
    }
  }
}


/*******************************************************************************
 * Loads params
 */
void CIPR::loadParams()
{
  // The raw sensor measurements topic --- straight from the source
  nodehandle_.getParam(ros::this_node::getName() + "/base_scan_topic",
    base_scan_topic_);

  // The topic to which the interpolated scan will be published
  nodehandle_.getParam(ros::this_node::getName() + "/output_scan_topic",
    output_scan_topic_);

  // The value that ranges get if their id is invalid
  // **CAUTION** Must match the value of regular rays that happen to be
  // invalid. For the ydlidar tg30 this value is 0
  nodehandle_.getParam(ros::this_node::getName() + "/value_invalid",
    value_invalid_);

  // Either interpolate or just ignore the invalids
  nodehandle_.getParam(ros::this_node::getName() + "/do_interpolate",
    do_interpolate_);

  // The indices of rays that sense obstacles in the robot's body
  loadInvalidIds();
}


/*******************************************************************************
 * The laser scan callback
 */
void CIPR::scanCallback(const sensor_msgs::LaserScan::Ptr& scan_msg)
{
  if (invalid_ids_.size() > 0)
  {
    std::vector<float> scan_vector;
    for (unsigned int i = 0; i < scan_msg->ranges.size(); i++)
      scan_vector.push_back(scan_msg->ranges[i]);

    // Mark the invalid rays with `value_invalid_` (0 for tg30)
    for (unsigned int i = 0; i < invalid_ids_.size(); i++)
      scan_vector[invalid_ids_[i]] = value_invalid_;

    // The vector that will be outputted
    std::vector<float> interpolated_scan_vector;

    if (do_interpolate_)
      interpolateInvalidRaysInScan(scan_vector, &interpolated_scan_vector);
    else
      interpolated_scan_vector = scan_vector;

    // The interpolated laser scan to be published
    sensor_msgs::LaserScan out_msg = *scan_msg;
    out_msg.ranges = interpolated_scan_vector;

    scan_pub_.publish(out_msg);
  }
}
